// Get recently used emojis from localStorage
export function getRecentEmojis(): string[] {
  try {
    const stored = localStorage.getItem("github-chat-recent-emojis")
    return stored ? JSON.parse(stored) : []
  } catch {
    return []
  }
}

// Save emoji to recently used
export function saveRecentEmoji(emoji: string): void {
  try {
    let recent = getRecentEmojis()
    // Remove if already exists
    recent = recent.filter((e) => e !== emoji)
    // Add to front
    recent.unshift(emoji)
    // Keep only last 24
    recent = recent.slice(0, 24)
    localStorage.setItem("github-chat-recent-emojis", JSON.stringify(recent))
  } catch {
    // Ignore localStorage errors
  }
}

// Search emojis by keyword
export function searchEmojis(query: string): string[] {
  const lowerQuery = query.toLowerCase().trim()
  if (!lowerQuery) return []

  const results = new Set<string>()

  // Check keyword matches
  for (const [keyword, emojis] of Object.entries(EMOJI_KEYWORDS)) {
    if (keyword.includes(lowerQuery) || lowerQuery.includes(keyword)) {
      emojis.forEach((e) => results.add(e))
    }
  }

  // Also search through all emojis in categories
  for (const category of EMOJI_CATEGORIES) {
    if (category.id === "recent") continue
    if (category.name.toLowerCase().includes(lowerQuery)) {
      category.emojis.slice(0, 20).forEach((e) => results.add(e))
    }
  }

  return Array.from(results).slice(0, 50)
}
